CREATE OR REPLACE FUNCTION get_expiring_warranties(
  days_ahead integer DEFAULT 90,
  p_property_id bigint DEFAULT NULL
)
RETURNS TABLE (
  id uuid, property_id bigint, item_name text, warranty_type text,
  warranty_expiration_date date, days_until_expiry integer,
  contractor_name text, coverage_notes text, status text,
  alert_days_before integer
)
LANGUAGE plpgsql STABLE AS $$
BEGIN
  RETURN QUERY
  SELECT w.id, w.property_id, w.item_name, w.warranty_type,
    w.warranty_expiration_date,
    (w.warranty_expiration_date - CURRENT_DATE)::integer,
    w.contractor_name, w.coverage_notes, w.status, w.alert_days_before
  FROM warranty_records w
  WHERE w.status = 'active'
    AND w.warranty_expiration_date IS NOT NULL
    AND w.warranty_expiration_date BETWEEN CURRENT_DATE AND (CURRENT_DATE + days_ahead)
    AND (p_property_id IS NULL OR w.property_id = p_property_id)
  ORDER BY w.warranty_expiration_date ASC;
END;
$$;

CREATE OR REPLACE FUNCTION get_property_summary(p_id bigint)
RETURNS TABLE (
  property_id bigint, canonical_address text,
  total_leads bigint, total_maintenance bigint,
  total_warranties bigint, active_warranties bigint,
  open_disputes bigint, pending_inspections bigint,
  active_recurring bigint, last_event_date date,
  estimated_total_spend numeric
)
LANGUAGE plpgsql STABLE AS $$
BEGIN
  RETURN QUERY
  SELECT
    p.id,
    p.canonical_address,
    (SELECT COUNT(*) FROM leads l WHERE l.property_id = p.id),
    (SELECT COUNT(*) FROM maintenance_events me WHERE me.property_id = p.id),
    (SELECT COUNT(*) FROM warranty_records wr WHERE wr.property_id = p.id),
    (SELECT COUNT(*) FROM warranty_records wr WHERE wr.property_id = p.id AND wr.status = 'active'),
    (SELECT COUNT(*) FROM dispute_events de WHERE de.property_id = p.id AND de.status IN ('open','escalated','legal')),
    (SELECT COUNT(*) FROM inspection_records ir WHERE ir.property_id = p.id AND ir.follow_up_required = true),
    (SELECT COUNT(*) FROM recurring_issues ri WHERE ri.property_id = p.id AND ri.status IN ('active','recurring')),
    (SELECT MAX(entry_date) FROM property_timeline_entries te WHERE te.property_id = p.id),
    (SELECT COALESCE(SUM(me.cost_actual), 0) FROM maintenance_events me WHERE me.property_id = p.id)
  FROM properties p
  WHERE p.id = p_id;
END;
$$;

-- ── 13. Address normalizer (SQL version for backfill) ─────────────────────────

CREATE OR REPLACE FUNCTION normalize_address_for_backfill(
  p_street text, p_city text, p_state text, p_zip text
) RETURNS text LANGUAGE plpgsql IMMUTABLE AS $$
DECLARE v text;
BEGIN
  v := LOWER(TRIM(REGEXP_REPLACE(
    COALESCE(p_street,'') || ', ' || COALESCE(p_city,'') || ', ' ||
    COALESCE(p_state,'') || ' ' || LEFT(COALESCE(p_zip,''),5),
    '\s+', ' ', 'g'
  )));
  v := REGEXP_REPLACE(v, '\bstreet\b',    'st',   'g');
  v := REGEXP_REPLACE(v, '\bavenue\b',    'ave',  'g');
  v := REGEXP_REPLACE(v, '\bboulevard\b', 'blvd', 'g');
  v := REGEXP_REPLACE(v, '\bdrive\b',     'dr',   'g');
  v := REGEXP_REPLACE(v, '\broad\b',      'rd',   'g');
  v := REGEXP_REPLACE(v, '\bcourt\b',     'ct',   'g');
  v := REGEXP_REPLACE(v, '\blane\b',      'ln',   'g');
  v := REGEXP_REPLACE(v, '\bplace\b',     'pl',   'g');
  v := REGEXP_REPLACE(v, '\bterrace\b',   'ter',  'g');
  v := REGEXP_REPLACE(v, '\btrail\b',     'trl',  'g');
  v := REGEXP_REPLACE(v, '\bcircle\b',    'cir',  'g');
  v := REGEXP_REPLACE(v, '\bparkway\b',   'pkwy', 'g');
  v := REGEXP_REPLACE(v, '\bhighway\b',   'hwy',  'g');
  v := REGEXP_REPLACE(v, '\bnorth\b',     'n',    'g');
  v := REGEXP_REPLACE(v, '\bsouth\b',     's',    'g');
  v := REGEXP_REPLACE(v, '\beast\b',      'e',    'g');
  v := REGEXP_REPLACE(v, '\bwest\b',      'w',    'g');
  v := REGEXP_REPLACE(v, '\bnortheast\b', 'ne',   'g');
  v := REGEXP_REPLACE(v, '\bnorthwest\b', 'nw',   'g');
  v := REGEXP_REPLACE(v, '\bsoutheast\b', 'se',   'g');
  v := REGEXP_REPLACE(v, '\bsouthwest\b', 'sw',   'g');
  v := REGEXP_REPLACE(v, '\bapartment\b', 'apt',  'g');
  v := REGEXP_REPLACE(v, '\bsuite\b',     'ste',  'g');
  RETURN TRIM(v);
END;
$$;

-- ── 14. BACKFILL: leads → properties ─────────────────────────────────────────

DO $$
DECLARE
  r RECORD;
  v_canonical text;
  v_prop_id   bigint;
  v_state     char(2);
BEGIN
  FOR r IN
    SELECT id, address, city, state, zip, property_facts
    FROM leads
    WHERE property_id IS NULL
      AND COALESCE(TRIM(address), '') <> ''
  LOOP
    v_state := UPPER(LEFT(TRIM(COALESCE(r.state, 'XX')), 2));
    v_canonical := normalize_address_for_backfill(r.address, r.city, v_state, r.zip);

    SELECT id INTO v_prop_id
    FROM properties
    WHERE canonical_address = v_canonical
    LIMIT 1;

    IF v_prop_id IS NULL THEN
      INSERT INTO properties (
        canonical_address, street_line_1, city, state, zip,
        property_type, year_built, square_feet,
        bedrooms, bathrooms, lot_size, parcel_number,
        match_confidence, needs_review
      ) VALUES (
        v_canonical,
        COALESCE(r.address, ''),
        COALESCE(r.city, ''),
        v_state,
        LEFT(COALESCE(r.zip, ''), 5),
        r.property_facts->>'propertyType',
        r.property_facts->>'yearBuilt',
        r.property_facts->>'squareFeet',
        r.property_facts->>'bedrooms',
        r.property_facts->>'bathrooms',
        r.property_facts->>'lotSize',
        r.property_facts->>'parcelNumber',
        'high', false
      )
      RETURNING id INTO v_prop_id;
    END IF;

    UPDATE leads SET property_id = v_prop_id WHERE id = r.id;
  END LOOP;
END;
$$;

-- Flag same ZIP + street number duplicates
DO $$
DECLARE r RECORD;
BEGIN
  FOR r IN
    SELECT p.zip,
      SPLIT_PART(p.canonical_address, ' ', 1) AS street_num,
      ARRAY_AGG(p.id) AS prop_ids
    FROM properties p
    GROUP BY p.zip, SPLIT_PART(p.canonical_address, ' ', 1)
    HAVING COUNT(*) > 1
      AND SPLIT_PART(MAX(p.canonical_address), ' ', 1) ~ '^\d'
  LOOP
    UPDATE properties
    SET needs_review = true,
        review_reason = 'Multiple property records share this ZIP + street number. Possible duplicate — review and merge if appropriate.'
    WHERE id = ANY(r.prop_ids);
  END LOOP;
END;
$$;

