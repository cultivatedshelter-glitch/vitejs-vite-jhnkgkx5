    ARRAY['job_execution_step_learning', 'work_type'],
    ARRAY['pricing_memory_entries', 'work_type']
  ];
  pair text[];
BEGIN
  FOREACH pair SLICE 1 IN ARRAY required_columns LOOP
    IF to_regclass(format('public.%I', pair[1])) IS NOT NULL AND NOT EXISTS (
      SELECT 1 FROM information_schema.columns
      WHERE table_schema = 'public'
        AND table_name = pair[1]
        AND column_name = pair[2]
    ) THEN
      RAISE EXCEPTION 'Required column public.%.% is missing', pair[1], pair[2];
    END IF;
  END LOOP;
END $$;
