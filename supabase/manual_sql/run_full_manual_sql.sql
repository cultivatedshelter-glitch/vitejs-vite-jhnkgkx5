-- ── 8. Pricing memory review RPC ──────────────────────────────────────────────

CREATE OR REPLACE FUNCTION get_pricing_memory_for_review(
  p_item_names text[],
  p_zip        text DEFAULT NULL,
  p_work_type  text DEFAULT NULL
)
RETURNS TABLE (
  item_name            text,
  verified_price       numeric,
  unit                 text,
  supplier_name        text,
  supplier_sku         text,
  supplier_product_url text,
  waste_factor         numeric,
  zip                  text,
  work_type            text,
  times_approved       integer,
  last_approved_at     timestamptz,
  source               text,
  id                   text
)
LANGUAGE plpgsql STABLE AS $$
BEGIN
  RETURN QUERY
  SELECT
    p.item_name::text, COALESCE(p.verified_price, p.unit_cost)::numeric,
    p.unit::text, p.supplier_name::text, p.supplier_sku::text,
    p.supplier_product_url::text, p.waste_factor,
    p.zip::text, p.work_type::text,
    p.times_approved, p.last_approved_at,
    p.source::text, p.id::text
  FROM pricing_memory_entries p
  WHERE
    p.human_verified = true
    AND EXISTS (
      SELECT 1 FROM unnest(p_item_names) qn
      WHERE LOWER(p.item_name) ILIKE '%' || LOWER(SPLIT_PART(qn, ' ', 1)) || '%'
         OR LOWER(p.item_name) ILIKE '%' || LOWER(SPLIT_PART(qn, ' ', 2)) || '%'
    )
    AND (p_zip IS NULL OR p.zip IS NULL OR LEFT(p.zip,3) = LEFT(p_zip,3))
    AND (p_work_type IS NULL OR p.work_type IS NULL OR LOWER(p.work_type) ILIKE '%' || LOWER(p_work_type) || '%')
  ORDER BY p.times_approved DESC, p.last_approved_at DESC NULLS LAST
  LIMIT 60;
END;
$$;

-- ── 9. Trigger: increment times_approved when GC approves ────────────────────

CREATE OR REPLACE FUNCTION fn_increment_memory_approval()
RETURNS TRIGGER LANGUAGE plpgsql AS $$
BEGIN
  IF NEW.memory_entry_id IS NOT NULL AND NEW.memory_saved = true
     AND (OLD.memory_saved = false OR OLD.memory_saved IS NULL)
  THEN
    UPDATE pricing_memory_entries
    SET
      times_approved   = COALESCE(times_approved, 0) + 1,
      last_approved_at = NOW(),
      last_approved_price = COALESCE(NEW.final_unit_price, NEW.unit_price)
    WHERE id::text = NEW.memory_entry_id;
  END IF;
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS trg_increment_memory_approval ON material_draft_items;
CREATE TRIGGER trg_increment_memory_approval
  AFTER UPDATE OF memory_saved ON material_draft_items
  FOR EACH ROW EXECUTE FUNCTION fn_increment_memory_approval();

COMMIT;



